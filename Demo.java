class Demo {
		public static void main(String[] args){
			
				byte age=10;
				System.out.println("byte类型"+age);
				
				short personCount=1000;
				System.out.println("short类型"+personCount);
				
				//定义一个float类型,注意后加f
				float price=12.5f;
				System.out.println("float类型"+price);
				
				//数值后加不加D都可以
				double totalprice=45.5;
				System.out.println("double类型"+totalprice);
				
			  //字符类型占用两个字节
			  char c ='哈';
			  System.out.println("char类型"+c);
				
				//布尔数据类型
				boolean isMarry = true;
				System.out.println("布尔类型"+isMarry);
				
				
				
				}
				
				
				
	}
	
	
	 